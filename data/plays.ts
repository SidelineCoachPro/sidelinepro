export type PlayCategory = 'halfcourt' | 'inbounds' | 'defense' | 'special' | 'transition' | 'timeout'
export type PlayDifficulty = 'beg' | 'int' | 'adv'

export interface PlayAgent {
  id: string
  label: string
  isDefender?: boolean
  startX: number
  startY: number
  endX: number
  endY: number
}

export interface PlayStep {
  step: number
  description: string
}

export interface Play {
  id: string
  name: string
  category: PlayCategory
  categoryColor: string
  difficulty: PlayDifficulty
  type: string
  description: string
  teachingKeys: string[]
  steps: PlayStep[]
  agents: PlayAgent[]
  ballStartX: number
  ballStartY: number
  ballEndX: number
  ballEndY: number
  suggestedDurationMins: number
  videoUrl?: string
}

